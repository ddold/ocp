public class Overview {
    /**
     * - Instance initializers are also used to define instance variables
     * - They are defined with a code block of {}
     * - Multiple initializers defined in a class
     * - There invoked before the constructor
     * - Initializer blocks are executed after an implicit or explicit call to parents class constructor
     *
     * Reasons for instance initializers
     * - For a big class, handy to have variable
     *   initialization just after declaration
     * - All initializers invoked regardless of the
     *   constructor thats used to instantiate the object
     * - Initializers can be used to initialize variables
     *   of an anonymous class that can't define constructors
     */
}

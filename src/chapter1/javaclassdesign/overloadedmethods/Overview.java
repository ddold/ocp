public class Overview {
    /**
     * - Overloaded methods are methods are with the same name but different method parameter lists
     * - A class can overload its own methods and inherited methods from its base class
     * - Overloaded methods accept different lists of arguments
     * - The argument lists of overloaded methods can differ in terms of
     *   change in the number, type, or position of parameters they accept
     * - Overloaded methods are bound at compile time.
     *   Unlike overridden methods they're not bound at runtime
     * - A call to correctly overloaded methods can also fail compilation if the
     *   compiler is unable to resolve the call to an overloaded method
     * - Overloaded methods might define a different return type or access/nonaccess modifier,
     *   but they can't be defined with only a change in their return types or access or nonaccess modifiers
     */
}
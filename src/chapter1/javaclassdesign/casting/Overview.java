public class Overview {
    /**
     * - An instance can be implicity casted to its superclasses or interfaces that it implements
     * - An instance of nonfinal class can be explicitly casted to any interface at compile tile
     * - Classes in the same inheritance tree can be casted to each other using explicit casting at compile time
     * - Objects of classes that don't form part of the same inheritance tree cannot be casted
     * - Casting to an interface is successful at runtime if its class implements that interface
     * - Casting to a derived class type is successful at runtime if the casted object is actually
     *   a type of the derived class to which its casted
     */
}

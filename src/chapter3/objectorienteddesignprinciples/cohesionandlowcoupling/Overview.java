public class Overview {
    /**
     * - Cohesion refers to how focused a class or module is
     * - High cohesion refers to a well-focused class or module, whereas low cohesion refers to a
     *   class or module that doesn't have a well-defined responsibility
     * - Well-designed applications aim for highly cohesive classes or modules
     * - Coupling refers to how much a class or module knows about other classes or modules
     * - Loosely coupled classes interact with each other by using their interfaces (public methods)
     * - Low coupling and loose coupling refer to the same concept and are often used interchangeably
     * - Well-designed applications aim for loosely coupled classes and modules
     */
}
